    private void btnListaUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListaUsuariosActionPerformed
        
        t2.setColumnIdentifiers(titulos);
        t2.setRowCount(0);
        try {
            List<Object[]> l = u.listaUsuarios();
            
            //Llenar tabla
            for(Object[] fila: l){
                t2.addRow(fila);
            }

            tblRegistro.setModel(t2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"ERROR:\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnListaUsuariosActionPerformed

