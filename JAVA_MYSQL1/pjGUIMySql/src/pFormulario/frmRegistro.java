    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        //Validar
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre","ERROR",JOptionPane.WARNING_MESSAGE);
        } else {
            if(txtNombre.getText().matches("^[a-zA-ZáéíóúÁÉÍÓÚñÑ\s]+$")){
                
                String nombre = txtNombre.getText();
                //Registro
                u.registrarUsuario(nombre);
                
                txtNombre.setText("");
                txtNombre.requestFocus();
                
                
                //Añadir a la tabla
                t1.addRow(new Object[] {i,nombre});
                i+=1;
                
            } else {
                JOptionPane.showMessageDialog(null, "Asegurese de que no contenga caracteres inválidos","ERROR",JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
        
        //Validar
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre","ERROR",JOptionPane.WARNING_MESSAGE);
        } else {
            if(txtNombre.getText().matches("^[a-zA-ZáéíóúÁÉÍÓÚñÑ\s]+$")){
                
                String nombreC = txtNombre.getText();
                //Consulta
                u.consultarUsuario(nombreC);
                
                txtNombre.setText("");
                txtNombre.requestFocus();
                                
            } else {
                JOptionPane.showMessageDialog(null, "Asegurese de que no contenga caracteres inválidos","ERROR",JOptionPane.WARNING_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_btnConsultaActionPerformed

